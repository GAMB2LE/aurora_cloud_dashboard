def radar_field_colormap(field_name: str) -> str:
    """Return the Py-ART-style colormap name for a dashboard radar field."""
    if field_name in _RADAR_FIELD_COLORMAPS:
        return _RADAR_FIELD_COLORMAPS[field_name]
    lower = field_name.lower()
    if "dbz" in lower or lower in {"dbzh", "reflectivity"}:
        return "HomeyerRainbow"
    return "Carbone17"


def _linspace(start: float, stop: float, count: int) -> list[float]:
    if count <= 1:
        return [start]
    step = (stop - start) / (count - 1)
    return [start + step * idx for idx in range(count)]


def _homeyer_rainbow_channels(nc: int = 15) -> ChannelSpec:
    path1 = _linspace(0.8 * math.pi, 1.8 * math.pi, nc)
    path2 = _linspace(-0.33 * math.pi, 0.33 * math.pi, nc)
    first_count = nc * 2 // 5
    y_values = _linspace(0.3, 0.85, first_count) + _linspace(0.9, 0.0, nc - first_count)
    red: list[tuple[float, float]] = []
    green: list[tuple[float, float]] = []
    blue: list[tuple[float, float]] = []
    for idx, y_value in enumerate(y_values):
        u_value = 0.40 * math.sin(path1[idx])
        v_value = 0.55 * math.sin(path2[idx]) + 0.1
        x_value = idx / (len(y_values) - 1.0)
        red.append((x_value, y_value + 1.13983 * v_value))
        green.append((x_value, y_value - 0.39465 * u_value - 0.58060 * v_value))
        blue.append((x_value, y_value + 2.03211 * u_value))
    return {"red": tuple(red), "green": tuple(green), "blue": tuple(blue)}


def _channel_spec(colormap_name: str) -> ChannelSpec:
    if colormap_name == "HomeyerRainbow":
        return _homeyer_rainbow_channels()
    return _PYART_CHANNEL_DATA[colormap_name]


def _interp_channel(channel: Channel, value: float) -> float:
    if value <= channel[0][0]:
        return channel[0][1]
    if value >= channel[-1][0]:
        return channel[-1][1]
    idx = bisect_right(channel, (value, float("inf"))) - 1
    x0, y0 = channel[idx]
    x1, y1 = channel[idx + 1]
    if x1 == x0:
        return y0
    return y0 + (y1 - y0) * ((value - x0) / (x1 - x0))


def _clamp01(value: float) -> float:
    return min(1.0, max(0.0, value))


def _rgb_at(spec: ChannelSpec, value: float) -> tuple[int, int, int]:
    channels = (
        _interp_channel(spec["red"], value),
        _interp_channel(spec["green"], value),
        _interp_channel(spec["blue"], value),
    )
    return tuple(int(round(_clamp01(channel) * 255.0)) for channel in channels)


@lru_cache(maxsize=None)
def pyart_plotly_colorscale(colormap_name: str, samples: int = 33) -> tuple[tuple[float, str], ...]:
    """Return a Plotly colorscale sampled from a Py-ART-style colormap."""
    spec = _channel_spec(colormap_name)
    positions = _linspace(0.0, 1.0, samples)
    colorscale = []
    for position in positions:
        red, green, blue = _rgb_at(spec, position)
        colorscale.append((round(position, 6), f"rgb({red},{green},{blue})"))
    return tuple(colorscale)


def radar_plotly_colorscale(field_name: str) -> tuple[tuple[float, str], ...]:
    """Return the Plotly colorscale for a dashboard radar field."""
    return pyart_plotly_colorscale(radar_field_colormap(field_name))


def _matplotlib_segment_data(spec: ChannelSpec) -> dict[str, list[tuple[float, float, float]]]:
    return {
        channel: [(x_value, _clamp01(y_value), _clamp01(y_value)) for x_value, y_value in values]
        for channel, values in spec.items()
    }


@lru_cache(maxsize=None)
def pyart_matplotlib_colormap(colormap_name: str):
    """Return a Matplotlib colormap object for a Py-ART-style colormap."""
    import matplotlib.colors as colors

    return colors.LinearSegmentedColormap(
        f"pyart_{colormap_name}",
        _matplotlib_segment_data(_channel_spec(colormap_name)),
        N=256,
    )


def radar_matplotlib_colormap(field_name: str):
    """Return the Matplotlib colormap object for a dashboard radar field."""
    return pyart_matplotlib_colormap(radar_field_colormap(field_name))


def register_pyart_radar_colormaps(names: Iterable[str] | None = None) -> None:
    """Register the dashboard's Py-ART-style colormaps with Matplotlib."""
    import matplotlib as mpl

    colormap_names = set(names or _PYART_CHANNEL_DATA)
    colormap_names.add("HomeyerRainbow")
    for name in colormap_names:
        cmap = pyart_matplotlib_colormap(name)
        try:
            mpl.colormaps.register(name=f"pyart_{name}", cmap=cmap, force=True)
            mpl.colormaps.register(name=name, cmap=cmap, force=True)
        except AttributeError:
            mpl.cm.register_cmap(name=f"pyart_{name}", cmap=cmap)
            mpl.cm.register_cmap(name=name, cmap=cmap)
